// Manipulação Avançada de Arrays
// 1. Exercício de Map:
// Dado o array de números [25, 42, 13, 24, 51], utilize o método map para retornar
// um novo array com cada número multiplicado por 2.

const numeros = [25, 42, 13, 24, 51];

const numerosMultiplicados = numeros.map(numero => numero * 2)

console.log(numerosMultiplicados)
